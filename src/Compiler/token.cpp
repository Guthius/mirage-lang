#include "token.hpp"

auto token_kind_name(const TokenKind kind) -> const char * {
    switch (kind) {
    case TokenKind::IntLiteral:      return "INT";
    case TokenKind::FloatLiteral:    return "FLOAT";
    case TokenKind::StringLiteral:   return "STRING";
    case TokenKind::Identifier:      return "IDENT";
    case TokenKind::KwConst:         return "KW_CONST";
    case TokenKind::KwMut:           return "KW_MUT";
    case TokenKind::KwFn:            return "KW_FN";
    case TokenKind::KwType:          return "KW_TYPE";
    case TokenKind::KwTrait:         return "KW_TRAIT";
    case TokenKind::KwStruct:        return "KW_STRUCT";
    case TokenKind::KwImpl:          return "KW_IMPL";
    case TokenKind::KwFor:           return "KW_FOR";
    case TokenKind::KwPub:           return "KW_PUB";
    case TokenKind::KwContinue:      return "KW_CONTINUE";
    case TokenKind::KwReturn:        return "KW_RETURN";
    case TokenKind::KwIf:            return "KW_IF";
    case TokenKind::KwElse:          return "KW_ELSE";
    case TokenKind::KwWhile:         return "KW_WHILE";
    case TokenKind::KwIn:            return "KW_IN";
    case TokenKind::KwImport:        return "KW_IMPORT";
    case TokenKind::KwNamespace:     return "KW_NAMESPACE";
    case TokenKind::KwAsm:           return "KW_ASM";
    case TokenKind::KwMacro:         return "KW_MACRO";
    case TokenKind::KwTry:           return "KW_TRY";
    case TokenKind::KwWhen:          return "KW_WHEN";
    case TokenKind::KwNil:           return "KW_NIL";
    case TokenKind::KwTrue:          return "KW_TRUE";
    case TokenKind::KwFalse:         return "KW_FALSE";
    case TokenKind::KwSizeOf:        return "KW_SIZEOF";
    case TokenKind::KwOffsetOf:      return "KW_OFFSETOF";
    case TokenKind::KwCast:          return "KW_CAST";
    case TokenKind::KwEnum:          return "KW_ENUM";
    case TokenKind::KwIota:          return "KW_IOTA";
    case TokenKind::KwDefault:       return "KW_DEFAULT";
    case TokenKind::KwUndefined:     return "KW_UNDEFINED";
    case TokenKind::KwLen:           return "KW_LEN";
    case TokenKind::KwU8:            return "KW_U8";
    case TokenKind::KwU16:           return "KW_U16";
    case TokenKind::KwU32:           return "KW_U32";
    case TokenKind::KwU64:           return "KW_U64";
    case TokenKind::KwI8:            return "KW_I8";
    case TokenKind::KwI16:           return "KW_I16";
    case TokenKind::KwI32:           return "KW_I32";
    case TokenKind::KwI64:           return "KW_I64";
    case TokenKind::KwF32:           return "KW_F32";
    case TokenKind::KwF64:           return "KW_F64";
    case TokenKind::KwUSize:         return "KW_USIZE";
    case TokenKind::KwBool:          return "KW_BOOL";
    case TokenKind::KwByte:          return "KW_BYTE";
    case TokenKind::KwError:         return "KW_ERROR";
    case TokenKind::KwAnyptr:        return "KW_ANYPTR";
    case TokenKind::Plus:            return "PLUS";
    case TokenKind::Minus:           return "MINUS";
    case TokenKind::Star:            return "STAR";
    case TokenKind::Slash:           return "SLASH";
    case TokenKind::Percent:         return "PERCENT";
    case TokenKind::Ampersand:       return "AMP";
    case TokenKind::Pipe:            return "PIPE";
    case TokenKind::Caret:           return "CARET";
    case TokenKind::Tilde:           return "TILDE";
    case TokenKind::ShiftLeft:       return "SHL";
    case TokenKind::ShiftRight:      return "SHR";
    case TokenKind::EqualEqual:      return "EQ_EQ";
    case TokenKind::BangEqual:       return "BANG_EQ";
    case TokenKind::Less:            return "LESS";
    case TokenKind::Greater:         return "GREATER";
    case TokenKind::LessEqual:       return "LESS_EQ";
    case TokenKind::GreaterEqual:    return "GREATER_EQ";
    case TokenKind::AmpAmp:          return "AMP_AMP";
    case TokenKind::PipePipe:        return "PIPE_PIPE";
    case TokenKind::Bang:            return "BANG";
    case TokenKind::Equal:           return "EQ";
    case TokenKind::PlusEqual:       return "PLUS_EQ";
    case TokenKind::MinusEqual:      return "MINUS_EQ";
    case TokenKind::StarEqual:       return "STAR_EQ";
    case TokenKind::SlashEqual:      return "SLASH_EQ";
    case TokenKind::AmpEqual:        return "AMP_EQ";
    case TokenKind::PipeEqual:       return "PIPE_EQ";
    case TokenKind::CaretEqual:      return "CARET_EQ";
    case TokenKind::ShiftLeftEqual:  return "SHL_EQ";
    case TokenKind::ShiftRightEqual: return "SHR_EQ";
    case TokenKind::PlusPlus:        return "PLUS_PLUS";
    case TokenKind::MinusMinus:      return "MINUS_MINUS";
    case TokenKind::ColonEqual:      return "COLON_EQ";
    case TokenKind::Arrow:           return "ARROW";
    case TokenKind::DotDot:          return "DOT_DOT";
    case TokenKind::Question:        return "QUESTION";
    case TokenKind::Colon:           return "COLON";
    case TokenKind::LParen:          return "LPAREN";
    case TokenKind::RParen:          return "RPAREN";
    case TokenKind::LBrace:          return "LBRACE";
    case TokenKind::RBrace:          return "RBRACE";
    case TokenKind::LBracket:        return "LBRACKET";
    case TokenKind::RBracket:        return "RBRACKET";
    case TokenKind::Comma:           return "COMMA";
    case TokenKind::Dot:             return "DOT";
    case TokenKind::Semicolon:       return "SEMICOLON";
    case TokenKind::AsmBlock:        return "ASM_BLOCK";
    case TokenKind::Eof:             return "EOF";
    }

    return "UNKNOWN";
}
